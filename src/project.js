import Task from './task';

export default class Project {
  constructor(name) {
    this.name = name;
    this.tasks = [];
  }

  getProject() {
    return this.name;
  }

  setProject(newName) {
    this.name = newName;
  }

  getTasks() {
    return this.tasks;
  }

  setTasks(tasks) {
    tasks.sort((a, b) => {
      let x = a.name.toLowerCase(),
        y = b.name.toLowerCase();
      return x == y ? 0 : x > y ? 1 : -1;
    });
    this.tasks = tasks;
  }

  getTask(task) {
    return this.tasks.find(({ name }) => name === task.name);
  }

  addTask(taskName, taskDesc) {
    this.tasks.push(new Task(taskName, taskDesc));
  }

  deleteTask(task) {
    const toDelete = this.tasks.find(({ name }) => name === task.name);
    this.tasks.splice(this.tasks.indexOf(toDelete), 1);
  }

  sortTasks() {}
}
